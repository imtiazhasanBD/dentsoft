const calculateDetailCost = (procedures) => {
  if (!Array.isArray(procedures)) return 0;
  return procedures.reduce((sum, procName) => {
    return sum + (procedureCosts[procName] || procedureCosts["Default Procedure"] || 0);
  }, 0);
};

