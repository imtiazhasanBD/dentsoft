// Function to initialize teeth state
export const initializeTeethState = (teethSet) => {
  const state = {};
  Object.values(teethSet).flat().forEach(toothNumber => {
    state[toothNumber] = { status: 'healthy', treatments: [] }; // 'healthy', 'in-progress', 'completed'
  });
  return state;
};


